import crypto from 'node:crypto';
import fs from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import process from 'node:process';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const defaultPrivateRepoPath = 'C:\\dev\\project\\postbaby-private';
const defaultPrivateSourcePath = path.join(defaultPrivateRepoPath, 'src', 'script.js');
const defaultPrivateServiceWorkerSourcePath = path.join(defaultPrivateRepoPath, 'sw.js');
const sourcePath = process.env.POSTBABY_PRIVATE_SCRIPT_SOURCE || defaultPrivateSourcePath;
const serviceWorkerSourcePath = process.env.POSTBABY_PRIVATE_SERVICE_WORKER_SOURCE || defaultPrivateServiceWorkerSourcePath;
const publicPath = path.join(repoRoot, 'js', 'script.js');
const serviceWorkerPath = path.join(repoRoot, 'sw.js');
const indexHtmlPath = path.join(repoRoot, 'index.html');
const versionJsPath = path.join(repoRoot, 'js', 'version.js');
const assetRevisionQueryPattern = /\?v=[0-9a-f]{12}/g;
const indexAssetUrlPattern = /(href|src)="(\.\/(?:css|js)\/[^"?]+)(?:\?v=[0-9a-f]{12})?"/g;
const versionAssetRevisionPattern = /const POSTBABY_ASSET_REVISION = '[0-9a-f]{12}';/;
const generatedBanner = [
    '/*',
    ' * Generated by npm run build:public-js from maintainer-local source.',
    ' * Do not edit this file directly.',
    ' */',
    ''
].join('\n');
const publishCriticalAssetSpecs = [
    { type: 'file', relativePath: 'index.html' },
    { type: 'file', relativePath: 'sw.js' },
    { type: 'dirExt', relativeDir: 'js', extension: '.js' },
    { type: 'dirExt', relativeDir: 'css', extension: '.css' },
    { type: 'dirExt', relativeDir: 'locales', extension: '.json' },
    { type: 'optionalFile', relativePath: 'manifest.json' }
];
const revisionDefinitionPattern = /const PUBLIC_JS_REVISION = '([0-9a-f]+)';/g;
const conflictMarkerPattern = /^(<<<<<<<|=======|>>>>>>>)(?: .*)?$/gm;
const obfuscatorOptions = [
    ['--compact', 'true'],
    ['--simplify', 'true'],
    ['--identifier-names-generator', 'hexadecimal'],
    ['--rename-globals', 'false'],
    ['--rename-properties', 'false'],
    ['--transform-object-keys', 'false'],
    ['--string-array', 'true'],
    ['--string-array-threshold', '0.35'],
    ['--string-array-rotate', 'true'],
    ['--string-array-shuffle', 'true'],
    ['--string-array-wrappers-count', '1'],
    ['--control-flow-flattening', 'false'],
    ['--dead-code-injection', 'false'],
    ['--split-strings', 'false'],
    ['--self-defending', 'false'],
    ['--debug-protection', 'false'],
    ['--disable-console-output', 'false'],
    ['--numbers-to-expressions', 'false'],
    ['--unicode-escape-sequence', 'false'],
    ['--target', 'browser'],
    ['--seed', '1']
];

function normalizeLineEndings(text) {
    return text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
}

function withTrailingNewline(text) {
    return text.endsWith('\n') ? text : `${text}\n`;
}

function normalizeRelativePath(value) {
    return value.replace(/\\/g, '/');
}

function relativePath(filePath) {
    return normalizeRelativePath(path.relative(repoRoot, filePath));
}

function stripAssetRevisionQueries(text) {
    return normalizeLineEndings(text).replace(assetRevisionQueryPattern, '');
}

function normalizeIndexHtmlForRevision(text) {
    return stripAssetRevisionQueries(text);
}

function normalizeVersionJsForRevision(text) {
    return normalizeLineEndings(text).replace(
        versionAssetRevisionPattern,
        'const POSTBABY_ASSET_REVISION = \'\';'
    );
}

function stampIndexHtml(text, revision) {
    const normalizedText = normalizeLineEndings(text);
    return normalizedText.replace(
        indexAssetUrlPattern,
        (match, attributeName, assetPath) => `${attributeName}="${assetPath}?v=${revision}"`
    );
}

function stampVersionJs(text, revision) {
    const normalizedText = normalizeLineEndings(text);
    if (!versionAssetRevisionPattern.test(normalizedText)) {
        throw new Error(`${relativePath(versionJsPath)} must define POSTBABY_ASSET_REVISION.`);
    }

    return normalizedText.replace(
        versionAssetRevisionPattern,
        `const POSTBABY_ASSET_REVISION = '${revision}';`
    );
}

async function buildDeployRevision(nextPublicScriptText) {
    const relativePaths = await collectPublishCriticalTextAssetPaths();
    const hash = crypto.createHash('sha256');

    for (const assetRelativePath of relativePaths) {
        if (assetRelativePath === 'sw.js') {
            continue;
        }

        hash.update(assetRelativePath);
        hash.update('\0');

        if (assetRelativePath === 'js/script.js') {
            hash.update(normalizeLineEndings(nextPublicScriptText));
        } else if (assetRelativePath === 'index.html') {
            const indexHtmlText = await readOptionalTextFile(indexHtmlPath);
            hash.update(normalizeIndexHtmlForRevision(indexHtmlText));
        } else if (assetRelativePath === 'js/version.js') {
            const versionJsText = await readOptionalTextFile(versionJsPath);
            hash.update(normalizeVersionJsForRevision(versionJsText));
        } else {
            const assetText = await readOptionalTextFile(path.join(repoRoot, assetRelativePath));
            hash.update(normalizeLineEndings(assetText));
        }

        hash.update('\0');
    }

    return hash.digest('hex').slice(0, 12);
}

function lineNumberAtIndex(text, index) {
    return text.slice(0, index).split('\n').length;
}

function findConflictMarkers(text) {
    const normalizedText = normalizeLineEndings(text);
    return [...normalizedText.matchAll(conflictMarkerPattern)].map((match) => ({
        marker: match[1],
        line: lineNumberAtIndex(normalizedText, match.index || 0)
    }));
}

function formatConflictMarkerFindings(findings) {
    return findings
        .flatMap((finding) =>
            finding.markers.map((marker) => `  ${finding.relativePath}:${marker.line} ${marker.marker}`)
        )
        .join('\n');
}

function assertTextHasNoConflictMarkers(text, label) {
    const markers = findConflictMarkers(text);
    if (markers.length === 0) {
        return;
    }

    const formattedMarkers = markers
        .map((marker) => `  line ${marker.line}: ${marker.marker}`)
        .join('\n');
    throw new Error(`${label} contains merge conflict markers.\n${formattedMarkers}`);
}

function getPublicJsRevisionMatches(text) {
    const normalizedText = normalizeLineEndings(text);
    return [...normalizedText.matchAll(revisionDefinitionPattern)].map((match) => ({
        fullMatch: match[0],
        revision: match[1],
        index: match.index || 0
    }));
}

function readSinglePublicJsRevision(text, label) {
    const matches = getPublicJsRevisionMatches(text);
    if (matches.length !== 1) {
        throw new Error(`${label} must contain exactly one PUBLIC_JS_REVISION definition; found ${matches.length}.`);
    }

    return matches[0].revision;
}

function replaceSinglePublicJsRevision(text, revision, label) {
    const normalizedText = normalizeLineEndings(text);
    const matches = getPublicJsRevisionMatches(normalizedText);
    if (matches.length !== 1) {
        throw new Error(`${label} must contain exactly one PUBLIC_JS_REVISION definition; found ${matches.length}.`);
    }

    const match = matches[0];
    return normalizedText.slice(0, match.index) +
        `const PUBLIC_JS_REVISION = '${revision}';` +
        normalizedText.slice(match.index + match.fullMatch.length);
}

function buildServiceWorkerText(templateServiceWorkerText, revision, label) {
    const normalizedTemplate = normalizeLineEndings(templateServiceWorkerText);
    assertTextHasNoConflictMarkers(normalizedTemplate, label);
    return replaceSinglePublicJsRevision(normalizedTemplate, revision, label);
}

function parseArgs(argv) {
    const candidateIndex = argv.indexOf('--candidate');
    const hasCandidate = candidateIndex !== -1;
    let candidatePath = null;

    if (hasCandidate) {
        const possiblePath = argv[candidateIndex + 1];
        if (possiblePath && !possiblePath.startsWith('--')) {
            candidatePath = path.resolve(possiblePath);
        } else {
            candidatePath = path.join(os.tmpdir(), `postbaby-public-script-${Date.now()}.js`);
        }
    }

    return {
        checkMode: argv.includes('--check'),
        candidatePath
    };
}

async function readTextFile(filePath) {
    return fs.readFile(filePath, 'utf8');
}

async function readRequiredTextFile(filePath, label) {
    try {
        return await readTextFile(filePath);
    } catch (error) {
        if (error && error.code === 'ENOENT') {
            throw new Error(`${label} is missing: ${filePath}`);
        }
        throw error;
    }
}

async function readOptionalTextFile(filePath) {
    try {
        return await readTextFile(filePath);
    } catch (error) {
        if (error && error.code === 'ENOENT') {
            return '';
        }
        throw error;
    }
}

async function exists(filePath) {
    try {
        await fs.access(filePath);
        return true;
    } catch (error) {
        if (error && error.code === 'ENOENT') {
            return false;
        }
        throw error;
    }
}

function flattenObfuscatorArgs(inputPath, outputPath) {
    return [
        inputPath,
        '--output',
        outputPath,
        ...obfuscatorOptions.flat()
    ];
}

function runObfuscator(inputPath, outputPath) {
    const obfuscatorCommand = process.platform === 'win32' ? 'javascript-obfuscator.cmd' : 'javascript-obfuscator';
    const obfuscatorArgs = flattenObfuscatorArgs(inputPath, outputPath);
    const command = process.platform === 'win32' ? 'cmd.exe' : obfuscatorCommand;
    const commandArgs = process.platform === 'win32'
        ? ['/d', '/s', '/c', obfuscatorCommand, ...obfuscatorArgs]
        : obfuscatorArgs;
    const result = spawnSync(command, commandArgs, {
        encoding: 'utf8',
        windowsHide: true
    });

    if (result.error) {
        if (result.error.code === 'ENOENT') {
            throw new Error(
                `${obfuscatorCommand} was not found on PATH. Install javascript-obfuscator or add it to PATH before building public JS.`
            );
        }
        throw result.error;
    }

    if (result.status !== 0) {
        throw new Error(
            `javascript-obfuscator failed with exit code ${result.status}.\n` +
            `${result.stdout || ''}${result.stderr || ''}`.trim()
        );
    }
}

async function buildPublicScript(sourceText) {
    const tempDir = await fs.mkdtemp(path.join(os.tmpdir(), 'postbaby-public-js-'));
    const tempSourcePath = path.join(tempDir, 'script.source.js');
    const tempOutputPath = path.join(tempDir, 'script.obfuscated.js');

    try {
        await fs.writeFile(tempSourcePath, normalizeLineEndings(sourceText), 'utf8');
        runObfuscator(tempSourcePath, tempOutputPath);
        const obfuscatedCode = await readTextFile(tempOutputPath);

        return withTrailingNewline(
            generatedBanner + normalizeLineEndings(obfuscatedCode).trimEnd()
        );
    } finally {
        await fs.rm(tempDir, { recursive: true, force: true });
    }
}

async function writeFileLoud(filePath, contents, label) {
    try {
        await fs.writeFile(filePath, contents, 'utf8');
    } catch (error) {
        const reason = error instanceof Error ? error.message : String(error);
        throw new Error(`Failed to write ${label} (${relativePath(filePath)}): ${reason}`);
    }
}

async function writeCandidate(candidatePath, nextPublicScript) {
    await fs.mkdir(path.dirname(candidatePath), { recursive: true });
    await writeFileLoud(candidatePath, nextPublicScript, 'candidate public JS');
}

async function listFilesWithExtension(relativeDir, extension) {
    const directoryPath = path.join(repoRoot, relativeDir);
    const entries = await fs.readdir(directoryPath, { withFileTypes: true });
    return entries
        .filter((entry) => entry.isFile() && entry.name.endsWith(extension))
        .map((entry) => normalizeRelativePath(path.join(relativeDir, entry.name)));
}

async function collectPublishCriticalTextAssetPaths() {
    const relativePaths = [];

    for (const spec of publishCriticalAssetSpecs) {
        if (spec.type === 'file') {
            relativePaths.push(spec.relativePath);
            continue;
        }

        if (spec.type === 'optionalFile') {
            if (await exists(path.join(repoRoot, spec.relativePath))) {
                relativePaths.push(spec.relativePath);
            }
            continue;
        }

        if (spec.type === 'dirExt') {
            const paths = await listFilesWithExtension(spec.relativeDir, spec.extension);
            relativePaths.push(...paths);
        }
    }

    return [...new Set(relativePaths.map(normalizeRelativePath))].sort();
}

async function assertNoConflictMarkersInPublishCriticalAssets(stage, excludedRelativePaths = new Set()) {
    const relativePaths = await collectPublishCriticalTextAssetPaths();
    const findings = [];

    for (const assetRelativePath of relativePaths) {
        if (excludedRelativePaths.has(assetRelativePath)) {
            continue;
        }

        const assetText = await readRequiredTextFile(
            path.join(repoRoot, assetRelativePath),
            `Publish-critical asset ${assetRelativePath}`
        );
        const markers = findConflictMarkers(assetText);
        if (markers.length > 0) {
            findings.push({
                relativePath: assetRelativePath,
                markers
            });
        }
    }

    if (findings.length === 0) {
        return;
    }

    throw new Error(
        `Merge conflict markers found in publish-critical text assets (${stage}).\n` +
        formatConflictMarkerFindings(findings)
    );
}

async function main() {
    const { checkMode, candidatePath } = parseArgs(process.argv.slice(2));

    console.log(`Readable source: ${sourcePath}`);
    console.log(`Readable service worker source: ${serviceWorkerSourcePath}`);
    console.log(`Public output: ${publicPath}`);

    if (candidatePath) {
        console.log(`Candidate output: ${candidatePath}`);
    }

    const [sourceText, serviceWorkerTemplateText, currentPublicScript, currentServiceWorker, currentIndexHtml, currentVersionJs] = await Promise.all([
        readRequiredTextFile(sourcePath, 'Readable private source'),
        readRequiredTextFile(serviceWorkerSourcePath, 'Readable service worker source'),
        readOptionalTextFile(publicPath),
        readRequiredTextFile(serviceWorkerPath, 'Public service worker'),
        readOptionalTextFile(indexHtmlPath),
        readOptionalTextFile(versionJsPath)
    ]);

    const nextPublicScript = await buildPublicScript(sourceText);
    const publicScriptChanged = currentPublicScript !== nextPublicScript;
    const nextRevision = await buildDeployRevision(nextPublicScript);
    const nextServiceWorker = buildServiceWorkerText(
        serviceWorkerTemplateText,
        nextRevision,
        relativePath(serviceWorkerSourcePath)
    );
    const serviceWorkerChanged = currentServiceWorker !== nextServiceWorker;
    const nextIndexHtml = currentIndexHtml ? stampIndexHtml(currentIndexHtml, nextRevision) : '';
    const nextVersionJs = currentVersionJs ? stampVersionJs(currentVersionJs, nextRevision) : '';
    const indexHtmlChanged = currentIndexHtml !== nextIndexHtml;
    const versionJsChanged = currentVersionJs !== nextVersionJs;

    if (candidatePath) {
        await writeCandidate(candidatePath, nextPublicScript);
        console.log(`Wrote candidate ${candidatePath} with revision ${nextRevision}.`);
        return;
    }

    if (checkMode) {
        assertTextHasNoConflictMarkers(currentServiceWorker, relativePath(serviceWorkerPath));
        readSinglePublicJsRevision(currentServiceWorker, relativePath(serviceWorkerPath));
        await assertNoConflictMarkersInPublishCriticalAssets('verify');

        const staleFiles = [];
        if (publicScriptChanged) {
            staleFiles.push(relativePath(publicPath));
        }
        if (serviceWorkerChanged) {
            staleFiles.push(relativePath(serviceWorkerPath));
        }
        if (indexHtmlChanged) {
            staleFiles.push(relativePath(indexHtmlPath));
        }
        if (versionJsChanged) {
            staleFiles.push(relativePath(versionJsPath));
        }

        if (staleFiles.length > 0) {
            console.error(
                `Public artifact drift detected: ${staleFiles.join(', ')}.\n` +
                'Run npm run build:public-js in a maintainer checkout that has the private readable source, then commit the result.'
            );
            process.exitCode = 1;
            return;
        }

        console.log(`Public js artifact is up to date with revision ${nextRevision}.`);
        return;
    }

    await assertNoConflictMarkersInPublishCriticalAssets(
        'before build',
        new Set([relativePath(publicPath), relativePath(serviceWorkerPath)])
    );

    if (publicScriptChanged) {
        await writeFileLoud(publicPath, nextPublicScript, 'generated public JS');
        console.log(`Updated ${relativePath(publicPath)}.`);
    } else {
        console.log(`${relativePath(publicPath)} is already up to date.`);
    }

    if (serviceWorkerChanged) {
        await writeFileLoud(serviceWorkerPath, nextServiceWorker, 'service-worker artifact');
        console.log(`Repaired ${relativePath(serviceWorkerPath)} from ${relativePath(serviceWorkerSourcePath)} -> ${nextRevision}.`);
    } else {
        console.log(`${relativePath(serviceWorkerPath)} revision already matches (${nextRevision}).`);
    }

    if (indexHtmlChanged) {
        await writeFileLoud(indexHtmlPath, nextIndexHtml, 'stamped index.html');
        console.log(`Updated ${relativePath(indexHtmlPath)} asset revision query strings.`);
    }

    if (versionJsChanged) {
        await writeFileLoud(versionJsPath, nextVersionJs, 'stamped version.js');
        console.log(`Updated ${relativePath(versionJsPath)} asset revision token.`);
    }

    const finalServiceWorkerText = await readRequiredTextFile(serviceWorkerPath, 'Public service worker');
    assertTextHasNoConflictMarkers(finalServiceWorkerText, relativePath(serviceWorkerPath));
    readSinglePublicJsRevision(finalServiceWorkerText, relativePath(serviceWorkerPath));
    await assertNoConflictMarkersInPublishCriticalAssets('after build');

    console.log(`Public JS revision: ${nextRevision}`);
}

main().catch((error) => {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
});
